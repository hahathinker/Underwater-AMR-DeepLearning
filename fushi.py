# ===================== 解析训练/验证数据 =====================
train_epochs, train_loss, train_acc = [], [], []
val_epochs, val_loss, val_acc = [], [], []
snr_values, snr_acc = [], []

# 匹配训练集
import re
for line in log_text.split('\n'):
    line = line.strip()
    # 训练集
    train_match = re.match(r'Epoch (\d+), train, Loss=([\d.]+) Acc=([\d.]+)', line)
    if train_match:
        train_epochs.append(int(train_match.group(1)))
        train_loss.append(float(train_match.group(2)))
        train_acc.append(float(train_match.group(3)))
    # 验证集
    val_match = re.match(r'Epoch (\d+), Val, Loss=([\d.]+) Acc=([\d.]+)', line)
    if val_match:
        val_epochs.append(int(val_match.group(1)))
        val_loss.append(float(val_match.group(2)))
        val_acc.append(float(val_match.group(3)))
    # 测试集SNR
    snr_match = re.match(r'snr=([-\d]+), Acc=([\d.]+)', line)
    if snr_match:
        snr_values.append(int(snr_match.group(1)))
        snr_acc.append(float(snr_match.group(2)))

# ===================== 绘图：训练 & 验证曲线 =====================
plt.rcParams['font.sans-serif'] = ['SimHei']
plt.rcParams['axes.unicode_minus'] = False

fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 5))

ax1.plot(train_epochs, train_loss, 'b-o', label='训练损失', linewidth=2)
ax1.plot(val_epochs, val_loss, 'r-o', label='验证损失', linewidth=2)
ax1.set_xlabel('Epoch')
ax1.set_ylabel('Loss')
ax1.set_title('训练/验证损失曲线')
ax1.legend()
ax1.grid(alpha=0.3)

ax2.plot(train_epochs, train_acc, 'b-o', label='训练准确率', linewidth=2)
ax2.plot(val_epochs, val_acc, 'r-o', label='验证准确率', linewidth=2)
ax2.set_xlabel('Epoch')
ax2.set_ylabel('Accuracy')
ax2.set_title('训练/验证准确率曲线')
ax2.legend()
ax2.grid(alpha=0.3)

plt.tight_layout()
plt.savefig('train_val_curve.png', dpi=300)
plt.show()

# ===================== 绘图：测试集SNR曲线 =====================
